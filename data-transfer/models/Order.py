class Order:

    def __init__(self, first_name, last_name, email, phone, delivery, poi_name, address, address_zipcode, address_deptcode, address_dept, address_region, content, created_at):
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.phone = phone
        self.delivery = delivery
        self.poi_name = poi_name
        self.address = address
        self.address_zipcode = address_zipcode
        self.address_deptcode = address_deptcode
        self.address_dept = address_dept
        self.address_region = address_region
        self.content = content
        self.created_at = created_at